static int slotSliderEvent(PARAM *p, int id, DATA *d, int val)
{
  if(p == NULL || id == 0 || d == NULL || val < -1000) return -1;
  //when a slider is changed we set the value for such gauge
  //value from slider is int val
  //we set same value on the gauge using pvSetValue 
  if(id==slider_therm){
     pvSetValue(p,thermometer,val);
  }
  else if(id==slider_tacho){
     pvSetValue(p,tachometer,val);
  }
  else if(id==slider_amp){
     pvSetValue(p,amperemeter,val);
  }
  else if(id==slider_multislider){
     pvSetValue(p,multislider,val);
  }

  return 0;

}

