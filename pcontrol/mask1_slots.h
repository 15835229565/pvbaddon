DATA;

static int passInput(PARAM *p, DATA *d)
{
  if(d->pass_orig[0] != '\0')
  {
    if(strcmp(d->pass_orig,rlpass(d->pass)) == 0) return 1;
    else
    {
      pvBeep(p);
      pvMessageBox(p,-1,BoxWarning,"Wrong password",1,0,0);
      return -1;
    }
  }
  pvBeep(p);
  pvMessageBox(p,-1,BoxWarning,"Wrong password\nThere is no original password\nPlease put in original password",1,0,0);
  return -1;
}

static int slotInit(PARAM *p, DATA *d)
{
  if(p == NULL || d == NULL) return -1;
  char *cptr;
  memset(d,0,sizeof(DATA));
  pvSetEchoMode(p,editPass,2); // password echo
  FILE *fp = fopen(BUS_ID,"r");
  if(fp != NULL)
  {
    fgets(d->pass_orig,sizeof(d->pass_orig)-1,fp);
    cptr = strstr(d->pass_orig,"\n");
    if(cptr != NULL) *cptr = '\0';
    fclose(fp);
  }
  USER *user = (USER *) p->user;
  user->view = 0;
  return 0;
}

