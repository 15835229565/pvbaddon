static int slotButtonEvent(PARAM *p, int id, DATA *d)
{
  if(p == NULL || id == 0 || d == NULL) return -1;
  USER *user = (USER *) p->user;
  if(id == buttonView)       { user->view = 1; return 2;}           // view only
  if(id == buttonLogin)      { if(passInput(p,d) != -1) return 2; } // login
  if(id == buttonChange)     { return 5; }                          // change password
  if(id == buttonViewEventLog) return 4;                            // show event log
  return 0;
}

